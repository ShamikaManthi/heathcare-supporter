    private Map<String, String> getFiltersMap() {
        Map<String, String> filter = new HashMap<>();
        if (cmbEventType.getSelectedIndex() != 0) {
            filter.put("EventType", cmbEventType.getSelectedItem().toString());
        }
        if (cmbQuality.getSelectedIndex() != 0) {
            filter.put("Quality", cmbQuality.getSelectedItem().toString());
        }
        if (cmbDistrict.getSelectedIndex() != 0) {
            filter.put("District", cmbDistrict.getSelectedItem().toString());
        }

        if (cmbPharmacy.getSelectedIndex() == 0) {
//            filter.put("CoatType", null);
        } else {
            filter.put("Pharmacy", cmbPharmacy.getSelectedItem().toString());
        }

        if (cmbType.getSelectedIndex() == 0) {
//            filter.put("Group", null);
        } else {
            filter.put("Type", cmbType.getSelectedItem().toString());
        }

        if (cmbLab.getSelectedIndex() == 0) {
//            filter.put("Shedding", null);
        } else {
            filter.put("Laboratory", cmbLab.getSelectedItem().toString());
        }

        if (cmbTreatment.getSelectedIndex() == 0) {
//            filter.put("Trainablity", null);
        } else {
            filter.put("TreatmentType", cmbTreatment.getSelectedItem().toString());
        }

        if (cmbDept.getSelectedIndex() == 0) {
//            filter.put("Trainablity", null);
        } else {
            filter.put("Department", cmbDept.getSelectedItem().toString());
        }

        return filter;
    }


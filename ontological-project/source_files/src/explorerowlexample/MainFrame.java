    private void updateTable(List<Hospital> queryResult) {

        for (Hospital d : queryResult) {
            SwingWorker<Void, Void> worker = new SwingWorker<Void, Void>() {
                @Override
                protected Void doInBackground() throws Exception {
                    // Simulate doing something useful.
                    d.downloadImage();
                    return null;
                }

                @Override
                protected void done() {
                    refreshTable();
                    System.out.println("done");
                }

            };
            worker.execute();
        }

    }

